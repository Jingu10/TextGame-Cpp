void ConsoleUI::PrintInventory(const Character& p) {
    PrintLine();
    std::cout << "🎒 [인벤토리 & 상태창]\n";
    std::cout << "- 공격속성: " << ElemToStr(p.GetAttackElement()) << "\n";
    std::cout << "- 총 공격력: " << p.GetTotalAttack() << " / 총 방어력: " << p.GetTotalDefense() << "\n\n";

    auto& inven = p.GetInventory();
    if (inven.empty()) std::cout << "인벤토리가 비어있습니다.\n";
    for (size_t i = 0; i < inven.size(); ++i) {
        std::cout << "[" << i + 1 << "] " << inven[i]->GetName()
            << " (보너스: " << inven[i]->GetBonus() << ", 속성: "
            << ElemToStr(inven[i]->GetElement()) << ")\n";
    }
}

