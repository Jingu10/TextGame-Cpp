void ConsoleUI::PrintSkillEffect(const std::string& skillName) {
    if (skillName.find("슬래시") != std::string::npos || skillName.find("검") != std::string::npos) {
        std::cout << "  💥 서걱! 💥\n";
        std::cout << "    /| ________________ \n";
        std::cout << "  O|===|* >________________>\n";
        std::cout << "    \\| \n";
    }
    else if (skillName.find("파이어") != std::string::npos || skillName.find("마법") != std::string::npos) {
        std::cout << "  🔥 화르륵! 🔥\n";
        std::cout << "      (  * ) \n";
        std::cout << "     (  *** )\n";
        std::cout << "      ( * )   \n";
    }
    else {
        std::cout << "  ✨ 쾅!! ✨\n";
        std::cout << "    \\  |  /  \n";
        std::cout << "   -  💥  -  \n";
        std::cout << "    /  |  \\  \n";
    }

    // 이펙트 출력 후 0.6초 대기 (타격감 생성!)
    //std::this_thread::sleep_for(std::chrono::milliseconds(600));

    std::cout << "      ";
    for (int i = 0; i < 3; ++i) {
        std::cout << ". ";
        std::cout.flush(); // 버퍼를 강제로 비워 화면에 즉시 출력하게 함
        std::this_thread::sleep_for(std::chrono::milliseconds(150)); // 0.15초 대기
    }
    std::cout << "\n";
}