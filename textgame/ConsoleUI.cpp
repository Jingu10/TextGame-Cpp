void ConsoleUI::PrintConsumables(const Character& p) {
    std::cout << "\n   [보유 포션 목록]\n";
    auto& inven = p.GetInventory();
    bool hasItem = false;
    for (size_t i = 0; i < inven.size(); ++i) {
        if (inven[i]->GetType() == ItemType::CONSUMABLE) {
            std::string tStr = (inven[i]->GetConsType() == ConsumableType::HP) ? "HP회복 " : "MP회복 ";
            std::cout << "   " << i + 1 << ". " << inven[i]->GetName() << " (" << tStr << inven[i]->GetBonus() << ")\n";
            hasItem = true;
        }
    }
    if (!hasItem) std::cout << "   사용할 수 있는 포션이 없습니다.\n";
}

void ConsoleUI::PrintQuests(const QuestManager& qm) {
    PrintLine();
    std::cout << "📜 [퀘스트 게시판]\n";
    auto& quests = qm.GetQuests();
    for (size_t i = 0; i < quests.size(); ++i) {
        auto& q = quests[i];
        std::cout << "   " << i + 1 << ". " << q.name << " : " << q.desc << " ";

        if (q.state == QuestState::NOT_ACCEPTED) std::cout << "[🆕 수락 가능]\n";
        else if (q.state == QuestState::IN_PROGRESS) std::cout << "(" << q.current << "/" << q.target << ") [🏃 진행 중]\n";
        else if (q.state == QuestState::CAN_REWARD) std::cout << "(" << q.current << "/" << q.target << ") [🎁 보상 수령 가능!]\n";
        else std::cout << "[✅ 완료됨]\n";
    }
}

